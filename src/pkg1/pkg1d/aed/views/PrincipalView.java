    private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem1ActionPerformed
        JFileChooser fileChooser = new JFileChooser();

        // Abre el di치logo de selecci칩n de archivo
        int returnValue = fileChooser.showOpenDialog(null);

        if (returnValue == JFileChooser.APPROVE_OPTION) {
            File selectedFile = fileChooser.getSelectedFile();

            try {
                String contenido = Utils.leerContenidoArchivo(selectedFile);
                Utils.createTextFile(selectedFile, contenido);
                ContarLetrasV view = new ContarLetrasV(selectedFile, contenido);
                view.setVisible(true);
            } catch (Exception e) {
                Utils.mostrarMensajeDeError("Error al abrir el fichero");
            }
        }
        
    }//GEN-LAST:event_jMenuItem1ActionPerformed

