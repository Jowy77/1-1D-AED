    private void crearFicheroButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_crearFicheroButtonActionPerformed
        JFileChooser fileChooser = new JFileChooser();

        // Abre el di치logo de selecci칩n de archivo
        int returnValue = fileChooser.showOpenDialog(null);

        if (returnValue == JFileChooser.APPROVE_OPTION) {
            // El usuario eligi칩 abrir un archivo existente
            File selectedFile = fileChooser.getSelectedFile();
            Utils.createTextFile(selectedFile);
            IntroducirTextoView view = new IntroducirTextoView(selectedFile, Utils.openTextFile(selectedFile));
            view.setVisible(true);
        } else {
            // El usuario eligi칩 crear un nuevo archivo
            JFileChooser saveFileChooser = new JFileChooser();
            int saveReturnValue = saveFileChooser.showSaveDialog(null);

            if (saveReturnValue == JFileChooser.APPROVE_OPTION) {
                File selectedFile = saveFileChooser.getSelectedFile();
                Utils.createTextFile(selectedFile);
                IntroducirTextoView view = new IntroducirTextoView(selectedFile, "");
                view.setVisible(true);
            } else {
                Utils.mostrarMensajeDeInformacion("EL USUARIO HA CANCELADO LA OPERACION.");
            }
        }
    }//GEN-LAST:event_crearFicheroButtonActionPerformed

